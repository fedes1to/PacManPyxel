    def level(self, value):
        self._level = value

    @property
    def grid(self):
        if self.level == 1:
            return self.grid1
        elif self.level == 2:
            return self.grid2
        elif self.level == 3:
            return self.grid3
    
    def level_image(self):
        if self.level == 1:
            return self.level1_image
        elif self.level == 2:
            return self.level2_image
        elif self.level == 3:
            return self.level3_image

    def pacman_eats(self, y_grid, x_grid):
        retval = 0
        if self.grid[y_grid][x_grid] == 2:
            self.grid[y_grid][x_grid] = 0
            retval = 10
        elif self.grid[y_grid][x_grid] == 3:
            self.grid[y_grid][x_grid] = 0
            retval = 50
        return retval
    
    def check_cage(self, y_grid, x_grid):
        if (y_grid == 23 and x_grid == 13): # Arreglo para el glitch del primer frame
            return True
        return y_grid > 12 and y_grid < 15 and x_grid > 13 and x_grid < 18

    def get_random_pos(self):
        # Encuentra las celdas vacías
        empty_spaces = [(y, x) for y in range(len(self.grid)) for x in range(len(self.grid[0])) if self.grid[y][x] == 0]
        
        if empty_spaces:
            y, x = 0, 0

            # Evitamos los bordes del mapa y la prisión del centro.
            while ((y < 1 or y > 29) or (x < 2 or x > 29) or (y < 17 and y > 11 and x > 11 and x < 20)):
                y, x = random.choice(empty_spaces)
            
            return y, x
        return None

    # Comprueba si hay paredes en la dirección que se quiere mover
    def check_walls(self, input_direction, direction, y_grid, x_grid, is_ghost=False):
        x_offset, y_offset = normal_direction[input_direction]
        x_offset2, y_offset2 = normal_direction[direction]

        # Pac-Man no puede pasar por las paredes de los fantasmas
        next_tile = self.grid[y_grid + y_offset][x_grid + x_offset]
        current_tile = self.grid[y_grid + y_offset2][x_grid + x_offset2]
        
        is_wall = next_tile == 1 or (not is_ghost and next_tile == 4)
        is_current_wall = current_tile == 1 or (not is_ghost and current_tile == 4)

        if not is_wall:
            return 1
        elif is_current_wall:
            return 2